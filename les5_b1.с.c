#include <stdio.h>

int i, input, sqr, cub;
int main()
{
    
    scanf("%d", &input);
    if (input <= 100) 
    {
       for (i=1; i<=input; i++) 
       {
       sqr = i*i;
       cub = i*i*i;
       printf("%d %d %d\n", i, sqr, cub);
       } 
    }
    else
    printf("введеное число, вне диапазона от 1 до 100\n");
    
    return 0;
}
