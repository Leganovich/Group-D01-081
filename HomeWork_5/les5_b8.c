#include <stdio.h>

int main()
{ 
int a, num, res=0;
scanf ("%d", &a);
   while (a>0)
   {
    num = a % 10; 
      if (num == 9)
         res ++;
         a /= 10;
   }  
   if (res == 1)
         printf ("YES\n");
   else
      printf ("NO\n");
   return 0;
}
